-- VDBAS LTT Database Initialization
-- Based on docs/ARCHITECTURE.md

-- Lệnh thanh toán (LTT)
CREATE TABLE LTT_PAYMENT (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRANS_ID VARCHAR2(50) UNIQUE,
    AMOUNT NUMBER(18,2) NOT NULL,
    CURRENCY VARCHAR2(3) DEFAULT 'VND',
    STATUS VARCHAR2(20) DEFAULT 'INIT',
    MAKER_ID VARCHAR2(50),
    CHECKER_ID VARCHAR2(50),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP
);

-- Nhật ký kiểm toán (Audit Log) với cơ chế Hash Chain
CREATE TABLE AUDIT_LOG (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ENTITY_NAME VARCHAR2(50) NOT NULL,
    ENTITY_ID NUMBER NOT NULL,
    ACTION VARCHAR2(20) NOT NULL,
    PAYLOAD CLOB,
    PREV_HASH VARCHAR2(64),
    CURRENT_HASH VARCHAR2(64),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Comment mô tả nghiệp vụ
COMMENT ON COLUMN LTT_PAYMENT.STATUS IS 'INIT, PENDING, APPROVED, REJECTED';
COMMENT ON COLUMN AUDIT_LOG.CURRENT_HASH IS 'SHA-256 hash of payload + prev_hash';
