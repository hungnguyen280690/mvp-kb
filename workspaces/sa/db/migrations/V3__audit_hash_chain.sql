    -- === Them ban ghi kiem toan moi vao chuoi hash ===
    -- Tu dong lay prev_hash tu ban ghi cuoi cung cua LTT_ID
    -- va tinh current_hash
    --
    -- p_ltt_id: ID cua LTT
    -- p_action: Hanh dong (CREATE, EDIT, SUBMIT, ...)
