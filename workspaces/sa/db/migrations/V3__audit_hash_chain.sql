    -- p_version_to: Phien ban sau
    --
    -- Tra ve: ID cua ban ghi audit hash vua tao
    FUNCTION APPEND_AUDIT(
        p_ltt_id          IN NUMBER,
        p_action          IN VARCHAR2,
        p_old_values      IN CLOB DEFAULT NULL,
        p_new_values      IN CLOB DEFAULT NULL,
        p_created_by      IN VARCHAR2,
        p_created_by_name IN VARCHAR2 DEFAULT NULL,
        p_ip_address      IN VARCHAR2 DEFAULT NULL,
        p_state_from      IN VARCHAR2 DEFAULT NULL,
        p_state_to        IN VARCHAR2 DEFAULT NULL,
        p_version_from    IN NUMBER DEFAULT NULL,
        p_version_to      IN NUMBER DEFAULT NULL
    ) RETURN NUMBER;

    -- === Xac minh toan bo chuoi hash cua mot LTT ===
    -- Kiem tra moi hash trong chuoi co hop le khong
    -- Neu co bat ky hash nao bi sua → tra ve FALSE
    --
    -- p_ltt_id: ID cua LTT can xac minh
    -- Tra ve: TRUE neu chuoi nguyen ven, FALSE neu co gian lan
    FUNCTION VERIFY_HASH_CHAIN(
        p_ltt_id IN NUMBER
    ) RETURN BOOLEAN;

    -- === Xac minh va tra ve ket qua chi tiet ===
    -- p_ltt_id: ID cua LTT can xac minh
    -- p_result: 'VALID' hoac 'INVALID'
