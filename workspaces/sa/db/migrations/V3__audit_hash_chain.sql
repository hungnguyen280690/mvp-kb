-- ---------------------------------------------------------------------------
-- Bang LTT_AUDIT_HASH — Chuoi hash cho moi ban ghi kiem toan
-- ---------------------------------------------------------------------------
CREATE TABLE LTT_AUDIT_HASH (
    -- Khoa chinh
    ID                      NUMBER(20)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- FK ve bang LTT
    LTT_ID                  NUMBER(20)      NOT NULL,

    -- Hanh dong (tuong ung voi LTT_AUDIT.ACTION)
    -- CREATE, EDIT, SUBMIT, APPROVE_CHECK, APPROVE, REJECT,
    -- CANCEL, SIGN, SEND, CONFIRM, REVERSE, DELETE, BLOCK, UNBLOCK
    ACTION                  VARCHAR2(30)    NOT NULL,

    -- Gia tri cu dang JSON — snapshot truoc khi thay doi
    OLD_VALUES              CLOB,

    -- Gia tri moi dang JSON — snapshot sau khi thay doi
    NEW_VALUES              CLOB,

    -- Hash cua ban ghi truoc do trong chuoi (lien ket)
    PREV_HASH               VARCHAR2(64)    NOT NULL,

    -- Hash hien tai — SHA-256(prev_hash + action + new_values + created_at)
    CURRENT_HASH            VARCHAR2(64)    NOT NULL,

    -- Nguoi thuc hien thao tac
    CREATED_BY              VARCHAR2(50)    NOT NULL,
    CREATED_BY_NAME         VARCHAR2(200),

    -- Thoi gian tao ban ghi
    CREATED_AT              TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,

    -- Dia chi IP nguoi thao tac
    IP_ADDRESS              VARCHAR2(45),

    -- Trang thai truoc
    STATE_FROM              VARCHAR2(25),
    -- Trang thai sau
    STATE_TO                VARCHAR2(25),

    -- Phien ban truoc/sau
    VERSION_FROM            NUMBER(10),
    VERSION_TO              NUMBER(10),

    -- === Constraints ===
    CONSTRAINT PK_LTT_AUDIT_HASH PRIMARY KEY (ID),
    CONSTRAINT FK_LTT_AUDIT_HASH_LTT FOREIGN KEY (LTT_ID)
        REFERENCES LTT (ID) ON DELETE CASCADE,

    -- Current hash phai unique — dam bao khong co 2 ban ghi cung hash
    CONSTRAINT UQ_LTT_AUDIT_HASH_CURRENT UNIQUE (CURRENT_HASH),

    -- OLD_VALUES va NEW_VALUES phai la JSON hop le (neu co gia tri)
    CONSTRAINT CK_LTT_HASH_OLD_JSON CHECK (OLD_VALUES IS NULL OR OLD_VALUES IS JSON),
    CONSTRAINT CK_LTT_HASH_NEW_JSON CHECK (NEW_VALUES IS NULL OR NEW_VALUES IS JSON)
);

