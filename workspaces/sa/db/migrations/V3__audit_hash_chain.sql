CREATE OR REPLACE PACKAGE BODY AUDIT_HASH_PKG IS

    ---------------------------------------------------------------------------
    -- Tinh SHA-256 hash — Helper function
    -- Dau vao: chuoi can hash
    -- Dau ra: hex string SHA-256 (64 ky tu)
    ---------------------------------------------------------------------------
    FUNCTION COMPUTE_HASH(
        p_input IN VARCHAR2
    ) RETURN VARCHAR2 IS
        v_hash RAW(32);
    BEGIN
        -- Su dung DBMS_CRYPTO (Oracle 19c)
        -- DBMS_CRYPTO.HASH_SH256 = 4
        v_hash := DBMS_CRYPTO.HASH(
            src => UTL_I18N.STRING_TO_RAW(p_input, 'AL32UTF8'),
            typ => DBMS_CRYPTO.HASH_SH256
        );
        RETURN LOWER(RAWTOHEX(v_hash));
    END COMPUTE_HASH;

    ---------------------------------------------------------------------------
    -- GET_LAST_HASH — Lay hash cuoi cung cua mot LTT
    ---------------------------------------------------------------------------
    FUNCTION GET_LAST_HASH(
        p_ltt_id IN NUMBER
    ) RETURN VARCHAR2 IS
        v_hash VARCHAR2(64);
    BEGIN
        SELECT CURRENT_HASH
        INTO   v_hash
        FROM   LTT_AUDIT_HASH
        WHERE  LTT_ID = p_ltt_id
        ORDER  BY CREATED_AT DESC
        FETCH  FIRST 1 ROWS ONLY;

        RETURN v_hash;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            -- Khong co ban ghi nao — tra ve hash goc (genesis)
            -- Hash goc la SHA-256 cua 'TT.OUT.MANUAL.GENESIS'
            RETURN COMPUTE_HASH('TT.OUT.MANUAL.GENESIS.' || TO_CHAR(p_ltt_id));
    END GET_LAST_HASH;

    ---------------------------------------------------------------------------
    -- APPEND_AUDIT — Them ban ghi kiem toan moi
    ---------------------------------------------------------------------------
    FUNCTION APPEND_AUDIT(
        p_ltt_id          IN NUMBER,
        p_action          IN VARCHAR2,
        p_old_values      IN CLOB,
        p_new_values      IN CLOB,
        p_created_by      IN VARCHAR2,
        p_created_by_name IN VARCHAR2,
        p_ip_address      IN VARCHAR2,
        p_state_from      IN VARCHAR2,
        p_state_to        IN VARCHAR2,
        p_version_from    IN NUMBER,
        p_version_to      IN NUMBER
    ) RETURN NUMBER IS
        v_id          NUMBER(20);
        v_prev_hash   VARCHAR2(64);
        v_current_hash VARCHAR2(64);
        v_timestamp_str VARCHAR2(30);
        v_new_values_str VARCHAR2(32767);
    BEGIN
        -- Lay hash cua ban ghi truoc do
        v_prev_hash := GET_LAST_HASH(p_ltt_id);

        -- Cat ngan new_values de tinh hash (toi da 32767 ky tu)
        IF p_new_values IS NOT NULL THEN
            IF DBMS_LOB.GETLENGTH(p_new_values) > 32767 THEN
                v_new_values_str := DBMS_LOB.SUBSTR(p_new_values, 32767, 1);
            ELSE
                v_new_values_str := TO_CHAR(p_new_values);
            END IF;
        ELSE
            v_new_values_str := '{}';
        END IF;

        -- Tinh hash: SHA-256(prev_hash || action || new_values || timestamp)
        v_timestamp_str := TO_CHAR(SYSTIMESTAMP, 'YYYY-MM-DDHH24MISSFF6');
        v_current_hash := COMPUTE_HASH(
            v_prev_hash || '|' ||
            p_action || '|' ||
            v_new_values_str || '|' ||
            v_timestamp_str
        );

        -- Chen ban ghi moi
        INSERT INTO LTT_AUDIT_HASH (
            LTT_ID, ACTION, OLD_VALUES, NEW_VALUES,
            PREV_HASH, CURRENT_HASH,
            CREATED_BY, CREATED_BY_NAME, CREATED_AT, IP_ADDRESS,
            STATE_FROM, STATE_TO, VERSION_FROM, VERSION_TO
        ) VALUES (
            p_ltt_id, p_action, p_old_values, p_new_values,
            v_prev_hash, v_current_hash,
            p_created_by, p_created_by_name, SYSTIMESTAMP, p_ip_address,
            p_state_from, p_state_to, p_version_from, p_version_to
        )
        RETURNING ID INTO v_id;

        RETURN v_id;
    END APPEND_AUDIT;

    ---------------------------------------------------------------------------
    -- VERIFY_HASH_CHAIN — Xac minh toan bo chuoi hash cua mot LTT
    ---------------------------------------------------------------------------
    FUNCTION VERIFY_HASH_CHAIN(
        p_ltt_id IN NUMBER
    ) RETURN BOOLEAN IS
        v_cursor      SYS_REFCURSOR;
        v_id          NUMBER(20);
        v_action      VARCHAR2(30);
        v_new_values  CLOB;
        v_prev_hash   VARCHAR2(64);
        v_current_hash VARCHAR2(64);
        v_created_at  TIMESTAMP;
        v_expected    VARCHAR2(64);
        v_timestamp_str VARCHAR2(30);
        v_new_values_str VARCHAR2(32767);

        v_prev_chain_hash VARCHAR2(64);
        v_first_row    BOOLEAN := TRUE;
        v_count        NUMBER := 0;
    BEGIN
        -- Hash goc (genesis) cho ban ghi dau tien
        v_prev_chain_hash := COMPUTE_HASH('TT.OUT.MANUAL.GENESIS.' || TO_CHAR(p_ltt_id));

        OPEN v_cursor FOR
            SELECT ID, ACTION, NEW_VALUES, PREV_HASH, CURRENT_HASH, CREATED_AT
            FROM   LTT_AUDIT_HASH
            WHERE  LTT_ID = p_ltt_id
            ORDER  BY CREATED_AT ASC;

        LOOP
            FETCH v_cursor INTO v_id, v_action, v_new_values, v_prev_hash, v_current_hash, v_created_at;
            EXIT WHEN v_cursor%NOTFOUND;
            v_count := v_count + 1;

            -- Kiem tra prev_hash co khop voi hash cua ban ghi truoc do khong
            IF v_prev_hash != v_prev_chain_hash THEN
                CLOSE v_cursor;
                RETURN FALSE;
            END IF;

            -- Tinh lai hash va so sanh
            v_timestamp_str := TO_CHAR(v_created_at, 'YYYY-MM-DDHH24MISSFF6');

            IF v_new_values IS NOT NULL THEN
                IF DBMS_LOB.GETLENGTH(v_new_values) > 32767 THEN
                    v_new_values_str := DBMS_LOB.SUBSTR(v_new_values, 32767, 1);
                ELSE
                    v_new_values_str := TO_CHAR(v_new_values);
                END IF;
            ELSE
                v_new_values_str := '{}';
            END IF;

            v_expected := COMPUTE_HASH(
                v_prev_chain_hash || '|' ||
                v_action || '|' ||
                v_new_values_str || '|' ||
                v_timestamp_str
            );

            IF v_current_hash != v_expected THEN
                CLOSE v_cursor;
                RETURN FALSE;
            END IF;

            -- Cap nhat chain hash cho ban ghi tiep theo
            v_prev_chain_hash := v_current_hash;
        END LOOP;

        CLOSE v_cursor;

        -- Khong co ban ghi nao — hop le
        RETURN TRUE;

    EXCEPTION
        WHEN OTHERS THEN
            IF v_cursor%ISOPEN THEN
                CLOSE v_cursor;
            END IF;
            -- Loi khi xac minh → coi nhu khong hop le
            RETURN FALSE;
    END VERIFY_HASH_CHAIN;

    ---------------------------------------------------------------------------
    -- VERIFY_HASH_CHAIN_DETAIL — Xac minh va tra ve chi tiet
    ---------------------------------------------------------------------------
    PROCEDURE VERIFY_HASH_CHAIN_DETAIL(
        p_ltt_id        IN  NUMBER,
        p_result        OUT VARCHAR2,
        p_checked_count OUT NUMBER,
        p_error_detail  OUT VARCHAR2
    ) IS
        v_cursor      SYS_REFCURSOR;
        v_id          NUMBER(20);
        v_action      VARCHAR2(30);
        v_new_values  CLOB;
        v_prev_hash   VARCHAR2(64);
        v_current_hash VARCHAR2(64);
        v_created_at  TIMESTAMP;
        v_expected    VARCHAR2(64);
        v_timestamp_str VARCHAR2(30);
        v_new_values_str VARCHAR2(32767);

        v_prev_chain_hash VARCHAR2(64);
        v_count        NUMBER := 0;
    BEGIN
        v_prev_chain_hash := COMPUTE_HASH('TT.OUT.MANUAL.GENESIS.' || TO_CHAR(p_ltt_id));

        OPEN v_cursor FOR
            SELECT ID, ACTION, NEW_VALUES, PREV_HASH, CURRENT_HASH, CREATED_AT
            FROM   LTT_AUDIT_HASH
            WHERE  LTT_ID = p_ltt_id
            ORDER  BY CREATED_AT ASC;

        LOOP
            FETCH v_cursor INTO v_id, v_action, v_new_values, v_prev_hash, v_current_hash, v_created_at;
            EXIT WHEN v_cursor%NOTFOUND;
            v_count := v_count + 1;

            IF v_prev_hash != v_prev_chain_hash THEN
                CLOSE v_cursor;
                p_result := 'INVALID';
                p_checked_count := v_count;
                p_error_detail := 'Ban ghi ID=' || v_id || ': prev_hash khong khop voi hash truoc do. ' ||
                                  'Thuc te=' || SUBSTR(v_prev_hash, 1, 16) || '... Ky vong=' ||
                                  SUBSTR(v_prev_chain_hash, 1, 16) || '...';
                RETURN;
            END IF;

            v_timestamp_str := TO_CHAR(v_created_at, 'YYYY-MM-DDHH24MISSFF6');

            IF v_new_values IS NOT NULL THEN
                IF DBMS_LOB.GETLENGTH(v_new_values) > 32767 THEN
                    v_new_values_str := DBMS_LOB.SUBSTR(v_new_values, 32767, 1);
                ELSE
                    v_new_values_str := TO_CHAR(v_new_values);
                END IF;
            ELSE
                v_new_values_str := '{}';
            END IF;

            v_expected := COMPUTE_HASH(
                v_prev_chain_hash || '|' ||
                v_action || '|' ||
                v_new_values_str || '|' ||
                v_timestamp_str
            );

            IF v_current_hash != v_expected THEN
                CLOSE v_cursor;
                p_result := 'INVALID';
                p_checked_count := v_count;
                p_error_detail := 'Ban ghi ID=' || v_id || ' (action=' || v_action || '): ' ||
                                  'current_hash khong khop. ' ||
                                  'Thuc te=' || SUBSTR(v_current_hash, 1, 16) || '... Ky vong=' ||
                                  SUBSTR(v_expected, 1, 16) || '...';
                RETURN;
            END IF;

            v_prev_chain_hash := v_current_hash;
        END LOOP;

        CLOSE v_cursor;

        p_result := 'VALID';
        p_checked_count := v_count;
        p_error_detail := NULL;

    EXCEPTION
        WHEN OTHERS THEN
            IF v_cursor%ISOPEN THEN
                CLOSE v_cursor;
            END IF;
            p_result := 'INVALID';
            p_checked_count := v_count;
            p_error_detail := 'Loi he thong: ' || SQLERRM;
    END VERIFY_HASH_CHAIN_DETAIL;

END AUDIT_HASH_PKG;
