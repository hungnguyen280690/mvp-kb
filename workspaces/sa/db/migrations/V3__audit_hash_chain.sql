-- de co dieu khien tot hon tren noi dung JSON
-- ============================================================================

-- Lua chon: De application layer tu goi AUDIT_HASH_PKG.APPEND_AUDIT()
-- vi trigger khong co du context (old/new values JSON, IP address).
-- Trigger duoi day chi ghi hanh dong khi trang thai thay doi.

CREATE OR REPLACE TRIGGER TRG_LTT_AUDIT_HASH_STATE
AFTER UPDATE OF STATE ON LTT
FOR EACH ROW
DECLARE
    v_new_values CLOB;
    v_old_values CLOB;
BEGIN
    -- Chi ghi khi state thay doi
    IF :NEW.STATE != :OLD.STATE THEN
        -- Tao JSON don gian cho old/new state
        v_old_values := '{"state":"' || :OLD.STATE || '"}';
        v_new_values := '{"state":"' || :NEW.STATE || '"}';

        AUDIT_HASH_PKG.APPEND_AUDIT(
            p_ltt_id     => :NEW.ID,
            p_action     => 'STATE_CHANGE',
            p_old_values => v_old_values,
            p_new_values => v_new_values,
            p_created_by => COALESCE(:NEW.UPDATED_BY, :NEW.CREATED_BY),
            p_state_from => :OLD.STATE,
            p_state_to   => :NEW.STATE,
            p_version_from => :OLD.VERSION,
            p_version_to   => :NEW.VERSION
        );
    END IF;
END;
