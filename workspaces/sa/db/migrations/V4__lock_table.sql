-- ---------------------------------------------------------------------------
-- Bang LTT_LOCK — Khoa phien (concurrent edit prevention)
-- Ho tro khoa phan tan qua nhieu node (OpenShift)
-- ---------------------------------------------------------------------------
CREATE TABLE LTT_LOCK (
    -- Khoa chinh
    ID                      NUMBER(20)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- FK ve bang LTT
    LTT_ID                  NUMBER(20)      NOT NULL,

    -- Ma nguoi giu khoa
    LOCKED_BY               VARCHAR2(50)    NOT NULL,

    -- Thoi gian bat dau giu khoa
    LOCKED_AT               TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,

    -- Thoi gian het han khoa (mac dinh 10 phut)
    LOCK_EXPIRES_AT         TIMESTAMP       NOT NULL,

    -- Loai khoa
    -- EDIT: Khi mo form sua LTT
    -- DELETE: Khi mo popup xac nhan xoa
    -- SIGN: Khi dang ky so
    LOCK_TYPE               VARCHAR2(10)    DEFAULT 'EDIT' NOT NULL,

    -- Dia chi IP cua nguoi giu khoa
    IP_ADDRESS              VARCHAR2(45),

    -- Thong tin session / node (cho distributed lock)
    SESSION_ID              VARCHAR2(100),

    -- === Constraints ===
    CONSTRAINT PK_LTT_LOCK PRIMARY KEY (ID),
    CONSTRAINT FK_LTT_LOCK_LTT FOREIGN KEY (LTT_ID)
        REFERENCES LTT (ID) ON DELETE CASCADE,

    -- Moi LTT chi duoc co 1 lock active theo loai (EDIT/DELETE/SIGN)
    -- (Dam bao boi unique index co dieu kien — xem phan index phia duoi)

    CONSTRAINT CK_LTT_LOCK_TYPE CHECK (LOCK_TYPE IN ('EDIT', 'DELETE', 'SIGN')),

    -- Lock phai co thoi gian het han > thoi gian bat dau
    CONSTRAINT CK_LTT_LOCK_EXPIRY CHECK (LOCK_EXPIRES_AT > LOCKED_AT)
);

-- === Comments cho bang LTT_LOCK ===
COMMENT ON TABLE  LTT_LOCK IS 'Khoa phien (pessimistic lock) — chan chinh sua dong thoi (VAL-033)';
COMMENT ON COLUMN LTT_LOCK.ID IS 'Khoa chinh tu tang';
COMMENT ON COLUMN LTT_LOCK.LTT_ID IS 'FK ve bang LTT';
COMMENT ON COLUMN LTT_LOCK.LOCKED_BY IS 'Ma nguoi giu khoa';
COMMENT ON COLUMN LTT_LOCK.LOCKED_AT IS 'Thoi gian bat dau giu khoa';
COMMENT ON COLUMN LTT_LOCK.LOCK_EXPIRES_AT IS 'Thoi gian het han khoa — mac dinh 10 phut';
COMMENT ON COLUMN LTT_LOCK.LOCK_TYPE IS 'Loai khoa: EDIT (sua), DELETE (xoa), SIGN (ky so)';
COMMENT ON COLUMN LTT_LOCK.IP_ADDRESS IS 'Dia chi IP cua nguoi giu khoa';
COMMENT ON COLUMN LTT_LOCK.SESSION_ID IS 'Ma session / node — ho tro distributed lock';

-- === Index cho bang LTT_LOCK ===
-- Tim kiem lock theo LTT_ID (kiem tra lock nhanh)
CREATE INDEX IDX_LTT_LOCK_LTT_ID ON LTT_LOCK (LTT_ID);

-- Tim kiem lock theo nguoi giu (don dep lock cu)
CREATE INDEX IDX_LTT_LOCK_LOCKED_BY ON LTT_LOCK (LOCKED_BY);

-- Tim kiem lock da het han (tu dong don dep)
CREATE INDEX IDX_LTT_LOCK_EXPIRED ON LTT_LOCK (LOCK_EXPIRES_AT);


-- ============================================================================
-- PL/SQL Package: LTT_LOCK_PKG — Quan ly khoa phien
-- ============================================================================
CREATE OR REPLACE PACKAGE LTT_LOCK_PKG IS

    -- === Thoi gian het han khoa mac dinh (10 phut) ===
    LOCK_DURATION_MINUTES CONSTANT NUMBER := 10;

    -- === Acquire lock — Gianh quyen chinh sua LTT ===
    -- p_ltt_id: ID cua LTT can khoa
    -- p_user_id: Ma nguoi yeu cau khoa
    -- p_lock_type: 'EDIT', 'DELETE', hoac 'SIGN'
    -- p_ip_address: Dia chi IP
    -- p_session_id: Ma session / node
    --
    -- Tra ve:
    --   0 = Thanh cong
    --   1 = Da bi nguoi khac giu khoa (tra ve thong tin nguoi giu)
    --   2 = Ban ghi khong ton tai hoac da bi xoa
    PROCEDURE ACQUIRE_LOCK(
        p_ltt_id       IN  NUMBER,
        p_user_id      IN  VARCHAR2,
        p_lock_type    IN  VARCHAR2 DEFAULT 'EDIT',
        p_ip_address   IN  VARCHAR2 DEFAULT NULL,
        p_session_id   IN  VARCHAR2 DEFAULT NULL,
        p_result       OUT NUMBER,
        p_locked_by    OUT VARCHAR2,
        p_locked_until OUT TIMESTAMP
    );

    -- === Release lock — Giai phong khoa ===
    -- p_ltt_id: ID cua LTT can giai phong
    -- p_user_id: Ma nguoi yeu cau giai phong (chi nguoi giu moi duoc giai phong)
    -- p_lock_type: Loai khoa can giai phong
    --
    -- Tra ve:
    --   0 = Thanh cong
    --   1 = Khong tim thay lock
    --   2 = Lock bi giu boi nguoi khac
    PROCEDURE RELEASE_LOCK(
        p_ltt_id    IN  NUMBER,
        p_user_id   IN  VARCHAR2,
        p_lock_type IN  VARCHAR2 DEFAULT 'EDIT',
        p_result    OUT NUMBER
    );

    -- === Kiem tra LTT co dang bi khoa khong ===
    -- p_ltt_id: ID cua LTT can kiem tra
    -- Tra ve: TRUE neu dang bi khoa (va chua het han)
    FUNCTION IS_LOCKED(
        p_ltt_id IN NUMBER
    ) RETURN BOOLEAN;

    -- === Kiem tra LTT co bi khoa boi nguoi khac khong ===
    -- p_ltt_id: ID cua LTT
    -- p_user_id: Ma nguoi can kiem tra
    -- Tra ve: TRUE neu bi nguoi khac khoa
    FUNCTION IS_LOCKED_BY_OTHER(
        p_ltt_id IN NUMBER,
        p_user_id IN VARCHAR2
    ) RETURN BOOLEAN;

    -- === Don dep lock da het han ===
    -- Xoa toan bo lock da qua thoi gian het han
    -- Tra ve so lock da don dep
    PROCEDURE CLEANUP_EXPIRED_LOCKS(
        p_cleaned_count OUT NUMBER
    );

    -- === Giai phong toan bo lock cua mot user ===
    -- Dung khi user logout hoac session het han
    PROCEDURE RELEASE_ALL_USER_LOCKS(
        p_user_id IN VARCHAR2
    );

    -- === Verfy optimistic lock ===
    -- Kiem tra version client co khop voi version server khong
    -- p_ltt_id: ID cua LTT
    -- p_expected_version: Version tu client (If-Match header)
    -- Tra ve: TRUE neu version khop, FALSE neu khong khop (VAL-036)
    FUNCTION VERIFY_OPTIMISTIC_LOCK(
        p_ltt_id           IN NUMBER,
        p_expected_version IN NUMBER
    ) RETURN BOOLEAN;

    -- === Cap nhat LTT voi optimistic lock ===
    -- Chi cap nhat neu version khop, sau do tang version
    -- p_ltt_id: ID cua LTT
    -- p_expected_version: Version tu client
    -- p_updated_by: Ma nguoi cap nhat
    -- p_update_sql: Cap nhat SQL (SET clause) — goi tu application
    --
    -- Tra ve: So dong da cap nhat (0 = version khong khop, 1 = thanh cong)
    -- Uu tien: Goi tu application layer thay vi dung ham nay de co dieu khien tot hon

END LTT_LOCK_PKG;
