CREATE OR REPLACE PACKAGE BODY LTT_LOCK_PKG IS

    ---------------------------------------------------------------------------
    -- ACQUIRE_LOCK — Gianh quyen chinh sua
    ---------------------------------------------------------------------------
    PROCEDURE ACQUIRE_LOCK(
        p_ltt_id       IN  NUMBER,
        p_user_id      IN  VARCHAR2,
        p_lock_type    IN  VARCHAR2 DEFAULT 'EDIT',
        p_ip_address   IN  VARCHAR2 DEFAULT NULL,
        p_session_id   IN  VARCHAR2 DEFAULT NULL,
        p_result       OUT NUMBER,
        p_locked_by    OUT VARCHAR2,
        p_locked_until OUT TIMESTAMP
    ) IS
        v_lock_id     NUMBER(20);
        v_locked_by   VARCHAR2(50);
        v_expires_at  TIMESTAMP;
    BEGIN
        -- Buoc 1: Don dep lock het han cua LTT nay
        DELETE FROM LTT_LOCK
        WHERE  LTT_ID = p_ltt_id
        AND    LOCK_TYPE = p_lock_type
        AND    LOCK_EXPIRES_AT < SYSTIMESTAMP;

        -- Buoc 2: Kiem tra lock active hien tai
        BEGIN
            SELECT ID, LOCKED_BY, LOCK_EXPIRES_AT
            INTO   v_lock_id, v_locked_by, v_expires_at
            FROM   LTT_LOCK
            WHERE  LTT_ID = p_ltt_id
            AND    LOCK_TYPE = p_lock_type
            FOR UPDATE NOWAIT;

            -- Da co lock active
            IF v_locked_by = p_user_id THEN
                -- Cung nguoi → gia han lock
                UPDATE LTT_LOCK
                SET    LOCK_EXPIRES_AT = SYSTIMESTAMP +
                         NUMTODSINTERVAL(LOCK_DURATION_MINUTES, 'MINUTE'),
                       IP_ADDRESS = p_ip_address,
                       SESSION_ID = p_session_id
                WHERE  ID = v_lock_id;

                p_result := 0;
                p_locked_by := p_user_id;
                p_locked_until := SYSTIMESTAMP +
                    NUMTODSINTERVAL(LOCK_DURATION_MINUTES, 'MINUTE');
                COMMIT;
                RETURN;
            ELSE
                -- Nguoi khac giu lock → tu choi
                p_result := 1;
                p_locked_by := v_locked_by;
                p_locked_until := v_expires_at;
                COMMIT;
                RETURN;
            END IF;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                NULL; -- Khong co lock → tiep tuc tao lock moi
        END;

        -- Buoc 3: Tao lock moi
        INSERT INTO LTT_LOCK (
            LTT_ID, LOCKED_BY, LOCKED_AT, LOCK_EXPIRES_AT,
            LOCK_TYPE, IP_ADDRESS, SESSION_ID
        ) VALUES (
            p_ltt_id, p_user_id, SYSTIMESTAMP,
            SYSTIMESTAMP + NUMTODSINTERVAL(LOCK_DURATION_MINUTES, 'MINUTE'),
            p_lock_type, p_ip_address, p_session_id
        );

        p_result := 0;
        p_locked_by := p_user_id;
        p_locked_until := SYSTIMESTAMP +
            NUMTODSINTERVAL(LOCK_DURATION_MINUTES, 'MINUTE');

        COMMIT;
    END ACQUIRE_LOCK;

    ---------------------------------------------------------------------------
    -- RELEASE_LOCK — Giai phong khoa
    ---------------------------------------------------------------------------
    PROCEDURE RELEASE_LOCK(
        p_ltt_id    IN  NUMBER,
        p_user_id   IN  VARCHAR2,
        p_lock_type IN  VARCHAR2 DEFAULT 'EDIT',
        p_result    OUT NUMBER
    ) IS
    BEGIN
        -- Chi xoa lock neu dung nguoi giu
        DELETE FROM LTT_LOCK
        WHERE  LTT_ID = p_ltt_id
        AND    LOCKED_BY = p_user_id
        AND    LOCK_TYPE = p_lock_type;

        IF SQL%ROWCOUNT > 0 THEN
            p_result := 0;  -- Thanh cong
        ELSE
            -- Kiem tra xem lock co ton tai khong (bi nguoi khac giu)
            FOR rec IN (
                SELECT LOCKED_BY FROM LTT_LOCK
                WHERE LTT_ID = p_ltt_id AND LOCK_TYPE = p_lock_type
            ) LOOP
                IF rec.LOCKED_BY != p_user_id THEN
                    p_result := 2;  -- Lock bi nguoi khac giu
                ELSE
                    p_result := 1;  -- Khong tim thay (da het han)
                END IF;
                COMMIT;
                RETURN;
            END LOOP;
            p_result := 1;  -- Khong tim thay lock
        END IF;

        COMMIT;
    END RELEASE_LOCK;

    ---------------------------------------------------------------------------
    -- IS_LOCKED — Kiem tra co dang bi khoa khong
    ---------------------------------------------------------------------------
    FUNCTION IS_LOCKED(
        p_ltt_id IN NUMBER
    ) RETURN BOOLEAN IS
        v_count NUMBER;
    BEGIN
        -- Don dep lock het han truoc
        DELETE FROM LTT_LOCK
        WHERE  LTT_ID = p_ltt_id
        AND    LOCK_EXPIRES_AT < SYSTIMESTAMP;

        SELECT COUNT(*)
        INTO   v_count
        FROM   LTT_LOCK
        WHERE  LTT_ID = p_ltt_id
        AND    LOCK_EXPIRES_AT >= SYSTIMESTAMP;

        RETURN v_count > 0;
    END IS_LOCKED;

    ---------------------------------------------------------------------------
    -- IS_LOCKED_BY_OTHER — Kiem tra co bi nguoi khac khoa khong
    ---------------------------------------------------------------------------
    FUNCTION IS_LOCKED_BY_OTHER(
        p_ltt_id  IN NUMBER,
        p_user_id IN VARCHAR2
    ) RETURN BOOLEAN IS
        v_count NUMBER;
    BEGIN
        DELETE FROM LTT_LOCK
        WHERE  LTT_ID = p_ltt_id
        AND    LOCK_EXPIRES_AT < SYSTIMESTAMP;

        SELECT COUNT(*)
        INTO   v_count
        FROM   LTT_LOCK
        WHERE  LTT_ID = p_ltt_id
        AND    LOCK_EXPIRES_AT >= SYSTIMESTAMP
        AND    LOCKED_BY != p_user_id;

        RETURN v_count > 0;
    END IS_LOCKED_BY_OTHER;

    ---------------------------------------------------------------------------
    -- CLEANUP_EXPIRED_LOCKS — Don dep lock het han
    ---------------------------------------------------------------------------
    PROCEDURE CLEANUP_EXPIRED_LOCKS(
        p_cleaned_count OUT NUMBER
    ) IS
    BEGIN
        DELETE FROM LTT_LOCK
        WHERE  LOCK_EXPIRES_AT < SYSTIMESTAMP;

        p_cleaned_count := SQL%ROWCOUNT;
        COMMIT;
    END CLEANUP_EXPIRED_LOCKS;

    ---------------------------------------------------------------------------
    -- RELEASE_ALL_USER_LOCKS — Giai phong toan bo lock cua user
    ---------------------------------------------------------------------------
    PROCEDURE RELEASE_ALL_USER_LOCKS(
        p_user_id IN VARCHAR2
    ) IS
    BEGIN
        DELETE FROM LTT_LOCK
        WHERE  LOCKED_BY = p_user_id;

        COMMIT;
    END RELEASE_ALL_USER_LOCKS;

    ---------------------------------------------------------------------------
    -- VERIFY_OPTIMISTIC_LOCK — Kiem tra version
    ---------------------------------------------------------------------------
    FUNCTION VERIFY_OPTIMISTIC_LOCK(
        p_ltt_id           IN NUMBER,
        p_expected_version IN NUMBER
    ) RETURN BOOLEAN IS
        v_actual_version NUMBER(10);
    BEGIN
        SELECT VERSION
        INTO   v_actual_version
        FROM   LTT
        WHERE  ID = p_ltt_id
        AND    IS_DELETED = 0;

        RETURN v_actual_version = p_expected_version;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RETURN FALSE;
    END VERIFY_OPTIMISTIC_LOCK;

END LTT_LOCK_PKG;
