-- Trigger de gui thong bao AQ khi co event PENDING moi
CREATE OR REPLACE TRIGGER TRG_OUTBOX_NOTIFY
AFTER INSERT ON OUTBOX
FOR EACH ROW
WHEN (NEW.STATUS = 'PENDING')
DECLARE
    v_enqueue_options  DBMS_AQ.ENQUEUE_OPTIONS_T;
    v_message_properties DBMS_AQ.MESSAGE_PROPERTIES_T;
    v_message_handle   RAW(16);
    v_jms_message      SYS.AQ$_JMS_TEXT_MESSAGE;
BEGIN
    v_jms_message := SYS.AQ$_JMS_TEXT_MESSAGE.construct;
    v_jms_message.set_text(
        '{"outbox_id":' || :NEW.ID ||
        ',"event_type":"' || :NEW.EVENT_TYPE ||
        '","aggregate_type":"' || :NEW.AGGREGATE_TYPE ||
        '","aggregate_id":' || :NEW.AGGREGATE_ID || '}'
    );
    v_message_properties.priority := 1;

    DBMS_AQ.ENQUEUE(
        queue_name         => 'OUTBOX_NOTIFY_Q',
        enqueue_options    => v_enqueue_options,
        message_properties => v_message_properties,
        payload            => v_jms_message,
        msgid              => v_message_handle
    );
END;
