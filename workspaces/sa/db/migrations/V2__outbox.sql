-- ---------------------------------------------------------------------------
-- Bang OUTBOX — Luu event truoc khi publish len MQ
-- ---------------------------------------------------------------------------
CREATE TABLE OUTBOX (
    -- Khoa chinh
    ID                      NUMBER(20)      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- Loai aggregate (ten bang): LTT, LTT_LINE_ITEM, ...
    AGGREGATE_TYPE          VARCHAR2(50)    NOT NULL,

    -- ID cua aggregate (FK logic, khong dung FK vat ly de giam coupling)
    AGGREGATE_ID            NUMBER(20)      NOT NULL,

    -- Loai su kien — tu events.yaml va states.yaml transitions
    -- Vi du: LTT_CREATED, LTT_SUBMITTED, LTT_APPROVED_CHECK, LTT_APPROVED,
    --        LTT_REJECTED, LTT_CANCELLED, LTT_SIGNED, LTT_SENT,
    --        LTT_CONFIRMED, LTT_POSTED, LTT_REVERSED, LTT_BLOCKED, LTT_UNBLOCKED
    EVENT_TYPE              VARCHAR2(50)    NOT NULL,

    -- Noi dung event dang JSON (Oracle 19c ho tro JSON type)
    -- Su dung CLOB voi IS JSON constraint de dam bao tinh hop le
    PAYLOAD                 CLOB            NOT NULL,

    -- Trang thai xu ly: PENDING, PUBLISHED, FAILED
    STATUS                  VARCHAR2(15)    DEFAULT 'PENDING' NOT NULL,

    -- So lan da thu publish
    RETRY_COUNT             NUMBER(3)       DEFAULT 0 NOT NULL,

    -- So lan thu toi da (mac dinh 3 theo BIZ-RETRY)
    MAX_RETRIES             NUMBER(3)       DEFAULT 3 NOT NULL,

    -- Thoi gian tao event
    CREATED_AT              TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,

    -- Thoi gian publish thanh cong
    PUBLISHED_AT            TIMESTAMP,

    -- Thoi gian xu ly tiep theo (cho retry voi backoff)
    NEXT_RETRY_AT           TIMESTAMP,

    -- Loi khi publish (neu FAILED)
    ERROR_MESSAGE           VARCHAR2(4000),

    -- Ma correlation — dung cho idempotency gateway (BIZ-RETRY)
    CORRELATION_ID          VARCHAR2(64),

    -- Kenh dich: LNH, SP, LKB (de dinh tuyen MQ queue)
    TARGET_CHANNEL          VARCHAR2(5),

    -- Ten MQ queue dich
    TARGET_QUEUE            VARCHAR2(50),

    -- Partition key (dung cho scale-out polling)
    PARTITION_KEY           VARCHAR2(50),

    -- Idempotency key — dam bao event khong bi xu ly 2 lan
    IDEMPOTENCY_KEY         VARCHAR2(64)    UNIQUE,

    -- Thong tin nguoi tao event
    CREATED_BY              VARCHAR2(50)    NOT NULL,

    -- === Constraints ===
    CONSTRAINT PK_OUTBOX PRIMARY KEY (ID),
    CONSTRAINT CK_OUTBOX_STATUS CHECK (STATUS IN ('PENDING', 'PUBLISHED', 'FAILED')),
    CONSTRAINT CK_OUTBOX_RETRY CHECK (RETRY_COUNT >= 0 AND RETRY_COUNT <= MAX_RETRIES),
    CONSTRAINT CK_OUTBOX_CHANNEL CHECK (TARGET_CHANNEL IS NULL OR TARGET_CHANNEL IN ('LNH', 'SP', 'LKB')),
    CONSTRAINT CK_OUTBOX_PAYLOAD_JSON CHECK (PAYLOAD IS JSON)
);

