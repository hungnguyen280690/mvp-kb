-- === Index cho bang OUTBOX ===
-- Index chinh cho polling: lay PENDING events sap xep theo thoi gian tao
CREATE INDEX IDX_OUTBOX_POLL ON OUTBOX (STATUS, CREATED_AT)
    COMPRESS 1;

-- Index cho retry: lay FAILED events da den thoi gian retry
CREATE INDEX IDX_OUTBOX_RETRY ON OUTBOX (STATUS, NEXT_RETRY_AT)
    WHERE STATUS = 'FAILED' AND RETRY_COUNT < MAX_RETRIES;

-- Index theo aggregate — truy van toan bo event cua mot LTT
CREATE INDEX IDX_OUTBOX_AGGREGATE ON OUTBOX (AGGREGATE_TYPE, AGGREGATE_ID);

-- Index theo correlation id — tim kiem callback tu NHNN/NHTM
CREATE INDEX IDX_OUTBOX_CORRELATION ON OUTBOX (CORRELATION_ID);

-- Index theo kenh dich — loc event theo kenh MQ
CREATE INDEX IDX_OUTBOX_CHANNEL ON OUTBOX (TARGET_CHANNEL, STATUS);


-- ============================================================================
-- PL/SQL Package: OUTBOX_PKG — Helper cho xu ly outbox
-- ============================================================================
CREATE OR REPLACE PACKAGE OUTBOX_PKG IS

    -- === Tao event moi trong outbox (goi trong cung transaction voi LTT update) ===
    -- p_aggregate_type: 'LTT'
    -- p_aggregate_id: LTT.ID
    -- p_event_type: 'LTT_SUBMITTED', 'LTT_APPROVED', ...
    -- p_payload: JSON string
    -- p_target_channel: 'LNH', 'SP', 'LKB' hoac NULL
    -- p_target_queue: 'LNH.SEND.Q', ... hoac NULL
    -- p_correlation_id: correlation id tu LTT
    -- p_created_by: ma nguoi tao
    PROCEDURE INSERT_EVENT(
        p_aggregate_type  IN VARCHAR2,
        p_aggregate_id    IN NUMBER,
        p_event_type      IN VARCHAR2,
        p_payload         IN CLOB,
        p_target_channel  IN VARCHAR2 DEFAULT NULL,
        p_target_queue    IN VARCHAR2 DEFAULT NULL,
        p_correlation_id  IN VARCHAR2 DEFAULT NULL,
        p_idempotency_key IN VARCHAR2 DEFAULT NULL,
        p_created_by      IN VARCHAR2
    );

    -- === Danh dau event da publish thanh cong ===
    PROCEDURE MARK_PUBLISHED(
        p_id              IN NUMBER
    );

    -- === Danh dau event that bai va len lich retry ===
    -- Backoff: 5s (lan 1), 15s (lan 2), 45s (lan 3)
    PROCEDURE MARK_FAILED(
        p_id              IN NUMBER,
        p_error_message   IN VARCHAR2
    );

    -- === Lay PENDING events de xu ly (polling) ===
    -- p_limit: so luong event toi da can lay
    -- Tra ve ref cursor
    PROCEDURE GET_PENDING_EVENTS(
        p_limit   IN NUMBER,
        p_events  OUT SYS_REFCURSOR
    );

    -- === Don sach events da xu ly (chay dinh ky) ===
    -- Xoa events da PUBLISHED cu hon p_retention_days ngay
    PROCEDURE CLEANUP_PUBLISHED(
        p_retention_days IN NUMBER DEFAULT 30
    );

END OUTBOX_PKG;
/


CREATE OR REPLACE PACKAGE BODY OUTBOX_PKG IS

    ---------------------------------------------------------------------------
    -- INSERT_EVENT — Tao event moi
    ---------------------------------------------------------------------------
    PROCEDURE INSERT_EVENT(
        p_aggregate_type  IN VARCHAR2,
        p_aggregate_id    IN NUMBER,
        p_event_type      IN VARCHAR2,
        p_payload         IN CLOB,
        p_target_channel  IN VARCHAR2,
        p_target_queue    IN VARCHAR2,
        p_correlation_id  IN VARCHAR2,
        p_idempotency_key IN VARCHAR2,
        p_created_by      IN VARCHAR2
    ) IS
    BEGIN
        INSERT INTO OUTBOX (
            AGGREGATE_TYPE, AGGREGATE_ID, EVENT_TYPE, PAYLOAD,
            STATUS, TARGET_CHANNEL, TARGET_QUEUE,
            CORRELATION_ID, IDEMPOTENCY_KEY, CREATED_BY
        ) VALUES (
            p_aggregate_type, p_aggregate_id, p_event_type, p_payload,
            'PENDING', p_target_channel, p_target_queue,
            p_correlation_id,
            COALESCE(p_idempotency_key,
                     p_aggregate_type || '_' || p_aggregate_id || '_' || p_event_type || '_' ||
                     TO_CHAR(SYSTIMESTAMP, 'YYYYMMDDHH24MISSFF3')),
            p_created_by
        );
    END INSERT_EVENT;

    ---------------------------------------------------------------------------
    -- MARK_PUBLISHED — Danh dau da publish thanh cong
    ---------------------------------------------------------------------------
    PROCEDURE MARK_PUBLISHED(
        p_id              IN NUMBER
    ) IS
    BEGIN
        UPDATE OUTBOX
        SET STATUS       = 'PUBLISHED',
            PUBLISHED_AT = SYSTIMESTAMP
        WHERE ID = p_id;

        IF SQL%NOTFOUND THEN
            RAISE_APPLICATION_ERROR(-20001, 'Outbox event not found: ID=' || p_id);
        END IF;
    END MARK_PUBLISHED;

    ---------------------------------------------------------------------------
    -- MARK_FAILED — Danh dau that bai, len lich retry
    -- Backoff: lan 1 = +5s, lan 2 = +15s, lan 3 = +45s (BIZ-RETRY)
    ---------------------------------------------------------------------------
    PROCEDURE MARK_FAILED(
        p_id              IN NUMBER,
        p_error_message   IN VARCHAR2
    ) IS
        v_retry_count     NUMBER(3);
        v_max_retries     NUMBER(3);
        v_next_retry      TIMESTAMP;
        v_backoff_seconds NUMBER(10);
    BEGIN
        SELECT RETRY_COUNT, MAX_RETRIES
        INTO   v_retry_count, v_max_retries
        FROM   OUTBOX
        WHERE  ID = p_id
        FOR UPDATE;

        v_retry_count := v_retry_count + 1;

        -- Tinh backoff: 5, 15, 45 giay (BIZ-RETRY)
        IF v_retry_count = 1 THEN
            v_backoff_seconds := 5;
        ELSIF v_retry_count = 2 THEN
            v_backoff_seconds := 15;
        ELSE
            v_backoff_seconds := 45;
        END IF;

        v_next_retry := SYSTIMESTAMP + NUMTODSINTERVAL(v_backoff_seconds, 'SECOND');

        IF v_retry_count >= v_max_retries THEN
            -- Het lan retry → danh dau FAILED, khong retry nua
            UPDATE OUTBOX
            SET STATUS        = 'FAILED',
                RETRY_COUNT   = v_retry_count,
                ERROR_MESSAGE = SUBSTR(p_error_message, 1, 4000),
                NEXT_RETRY_AT = NULL
            WHERE ID = p_id;
        ELSE
            -- Con lan retry → danh dau PENDING, len lich retry
            UPDATE OUTBOX
            SET STATUS        = 'PENDING',
                RETRY_COUNT   = v_retry_count,
                ERROR_MESSAGE = SUBSTR(p_error_message, 1, 4000),
                NEXT_RETRY_AT = v_next_retry
            WHERE ID = p_id;
        END IF;
    END MARK_FAILED;

    ---------------------------------------------------------------------------
    -- GET_PENDING_EVENTS — Lay events cho polling
    ---------------------------------------------------------------------------
    PROCEDURE GET_PENDING_EVENTS(
        p_limit   IN NUMBER,
        p_events  OUT SYS_REFCURSOR
    ) IS
    BEGIN
        OPEN p_events FOR
            SELECT ID, AGGREGATE_TYPE, AGGREGATE_ID, EVENT_TYPE, PAYLOAD,
                   TARGET_CHANNEL, TARGET_QUEUE, CORRELATION_ID, RETRY_COUNT
            FROM   OUTBOX
            WHERE  STATUS = 'PENDING'
            AND    (NEXT_RETRY_AT IS NULL OR NEXT_RETRY_AT <= SYSTIMESTAMP)
            ORDER  BY CREATED_AT ASC
            FETCH  FIRST p_limit ROWS ONLY
            FOR UPDATE SKIP LOCKED;
    END GET_PENDING_EVENTS;

    ---------------------------------------------------------------------------
    -- CLEANUP_PUBLISHED — Xoa events da xu ly cu hon N ngay
    ---------------------------------------------------------------------------
    PROCEDURE CLEANUP_PUBLISHED(
        p_retention_days IN NUMBER DEFAULT 30
    ) IS
    BEGIN
        DELETE FROM OUTBOX
        WHERE  STATUS = 'PUBLISHED'
        AND    PUBLISHED_AT < SYSTIMESTAMP - NUMTODSINTERVAL(p_retention_days, 'DAY');

        COMMIT;
    END CLEANUP_PUBLISHED;

END OUTBOX_PKG;
/


