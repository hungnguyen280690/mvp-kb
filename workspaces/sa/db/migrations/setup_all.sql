-- Triggers
CREATE OR REPLACE TRIGGER TRG_LTT_UPD_TIMESTAMP
BEFORE UPDATE ON LTT FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSTIMESTAMP;
    IF :NEW.VERSION = :OLD.VERSION THEN
        :NEW.VERSION := :OLD.VERSION + 1;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_LTT_LINE_ITEM_UPD
BEFORE UPDATE ON LTT_LINE_ITEM FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSTIMESTAMP;
END;
/

-- ============================================================================
-- V2: OUTBOX
-- ============================================================================
CREATE TABLE OUTBOX (
    ID NUMBER(20) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    AGGREGATE_TYPE VARCHAR2(50) NOT NULL,
    AGGREGATE_ID NUMBER(20) NOT NULL,
    EVENT_TYPE VARCHAR2(50) NOT NULL,
    PAYLOAD CLOB NOT NULL,
    STATUS VARCHAR2(15) DEFAULT 'PENDING' NOT NULL,
    RETRY_COUNT NUMBER(3) DEFAULT 0 NOT NULL,
    MAX_RETRIES NUMBER(3) DEFAULT 3 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    PUBLISHED_AT TIMESTAMP,
    NEXT_RETRY_AT TIMESTAMP,
    ERROR_MESSAGE VARCHAR2(4000),
    CORRELATION_ID VARCHAR2(64),
    TARGET_CHANNEL VARCHAR2(5),
    TARGET_QUEUE VARCHAR2(50),
    PARTITION_KEY VARCHAR2(50),
    IDEMPOTENCY_KEY VARCHAR2(64) UNIQUE,
    CREATED_BY VARCHAR2(50) NOT NULL,
    CONSTRAINT CK_OUTBOX_STATUS CHECK (STATUS IN ('PENDING', 'PUBLISHED', 'FAILED')),
    CONSTRAINT CK_OUTBOX_RETRY CHECK (RETRY_COUNT >= 0 AND RETRY_COUNT <= MAX_RETRIES),
    CONSTRAINT CK_OUTBOX_CHANNEL CHECK (TARGET_CHANNEL IS NULL OR TARGET_CHANNEL IN ('LNH', 'SP', 'LKB')),
    CONSTRAINT CK_OUTBOX_PAYLOAD_JSON CHECK (PAYLOAD IS JSON)
);

CREATE INDEX IDX_OUTBOX_POLL ON OUTBOX (STATUS, CREATED_AT) COMPRESS 1;
CREATE INDEX IDX_OUTBOX_AGGREGATE ON OUTBOX (AGGREGATE_TYPE, AGGREGATE_ID);
CREATE INDEX IDX_OUTBOX_CORRELATION ON OUTBOX (CORRELATION_ID);
CREATE INDEX IDX_OUTBOX_CHANNEL ON OUTBOX (TARGET_CHANNEL, STATUS);

-- ============================================================================
-- V3: AUDIT HASH CHAIN
-- ============================================================================
CREATE TABLE LTT_AUDIT_HASH (
    ID NUMBER(20) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LTT_ID NUMBER(20) NOT NULL,
    ACTION VARCHAR2(30) NOT NULL,
    OLD_VALUES CLOB,
    NEW_VALUES CLOB,
    PREV_HASH VARCHAR2(64) NOT NULL,
    CURRENT_HASH VARCHAR2(64) NOT NULL,
    CREATED_BY VARCHAR2(50) NOT NULL,
    CREATED_BY_NAME VARCHAR2(200),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    IP_ADDRESS VARCHAR2(45),
    STATE_FROM VARCHAR2(25),
    STATE_TO VARCHAR2(25),
    VERSION_FROM NUMBER(10),
    VERSION_TO NUMBER(10),
    CONSTRAINT FK_LTT_AUDIT_HASH_LTT FOREIGN KEY (LTT_ID) REFERENCES LTT (ID) ON DELETE CASCADE,
    CONSTRAINT UQ_LTT_AUDIT_HASH_CURRENT UNIQUE (CURRENT_HASH),
    CONSTRAINT CK_LTT_HASH_OLD_JSON CHECK (OLD_VALUES IS NULL OR OLD_VALUES IS JSON),
    CONSTRAINT CK_LTT_HASH_NEW_JSON CHECK (NEW_VALUES IS NULL OR NEW_VALUES IS JSON)
);

CREATE INDEX IDX_LTT_HASH_LTT_TIME ON LTT_AUDIT_HASH (LTT_ID, CREATED_AT);
CREATE INDEX IDX_LTT_HASH_PREV ON LTT_AUDIT_HASH (PREV_HASH);
CREATE INDEX IDX_LTT_HASH_CREATED_BY ON LTT_AUDIT_HASH (CREATED_BY, CREATED_AT DESC);

